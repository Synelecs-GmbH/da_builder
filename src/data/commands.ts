export const getCommandDef = (type: string): CommandDef | undefined =>
  COMMAND_DEFS.find(c => c.type === type);

export const getCommandsByCategory = (): Record<string, CommandDef[]> => {
  const result: Record<string, CommandDef[]> = {};
  for (const cmd of COMMAND_DEFS) {
    if (!result[cmd.category]) result[cmd.category] = [];
    result[cmd.category].push(cmd);
  }
  return result;
};
